export const getStateColor = (state: string): string => {
  const colors: Record<string, string> = {
    Draft: "text-gray-400",
    Open: "text-green-400",
    Committed: "text-blue-400",
    Revealed: "text-purple-400",
    Selected: "text-yellow-400",
    Settled: "text-emerald-400",
    Expired: "text-red-400",
  };
  return colors[state] || "text-gray-400";
};

